/*! Translate group name to gid. Return -1 if error or not found.
 *
 * @param[in]   name  Name of group
 * @param[out]  gid   Group id
 * @retval      0     OK
 * @retval     -1     Error. or not found
 */
int
group_name2gid(const char *name,
               gid_t      *gid)
{
    int           retval = -1;
    char          buf[1024];
    struct group  g0;
    struct group *gr = &g0;
    struct group *gtmp;

    gr = &g0;
    /* This leaks memory in ubuntu */
    if (getgrnam_r(name, gr, buf, sizeof(buf), &gtmp) < 0){
        clixon_err(OE_UNIX, errno, "getgrnam_r(%s)", name);
        goto done;
    }
    if (gtmp == NULL){
        clixon_err(OE_UNIX, 0, "No such group: %s", name);
        goto done;
    }
    if (gid)
        *gid = gr->gr_gid;
    retval = 0;
 done:
    return retval;
}

/*! Translate user name to uid. Return -1 if error or not found.
 *
 * @param[in]  name Name of user
 * @param[out] uid  User id
 * @retval     0    OK
 * @retval    -1    Error. or not found
 */
int
name2uid(const char *name,
         uid_t      *uid)
{
    int            retval = -1;
    char           buf[1024];
    struct passwd  pwbuf;
    struct passwd *pwbufp = NULL;

    if (getpwnam_r(name, &pwbuf, buf, sizeof(buf), &pwbufp) != 0){
        clixon_err(OE_UNIX, errno, "getpwnam_r(%s)", name);
        goto done;
    }
    if (pwbufp == NULL){
        clixon_err(OE_UNIX, 0, "No such user: %s", name);
        goto done;
    }
    if (uid)
        *uid = pwbufp->pw_uid;
    retval = 0;
 done:
    return retval;
}

/*! Translate uid to user name
 *
 * @param[in]  uid  User id
 * @param[out] name User name (Malloced, need to be freed)
 * @retval     0    OK
 * @retval    -1    Error. or not found
 */
int
uid2name(const uid_t uid,
         char      **name)
{
    int            retval = -1;
    char           buf[1024];
    struct passwd  pwbuf = {0,};
    struct passwd *pwbufp = NULL;

    if (getpwuid_r(uid, &pwbuf, buf, sizeof(buf), &pwbufp) != 0){
        clixon_err(OE_UNIX, errno, "getpwuid_r(%u)", uid);
        goto done;
    }
    if (pwbufp == NULL){
        clixon_err(OE_UNIX, ENOENT, "No such user: %u", uid);
        goto done;
    }

    if (name){
        if ((*name = strdup(pwbufp->pw_name)) == NULL){
            clixon_err(OE_UNIX, errno, "strdup");
            goto done;
        }
    }
    retval = 0;
 done:
    return retval;
}

/*! Return all OS group names for a given username
 *
 * Uses POSIX setgrent/getgrent/endgrent to iterate all groups and collect
 * those whose member list contains username, plus the user's primary group.
 * @param[in]   username  OS user name to look up
 * @param[out]  groupsp   Malloced array of malloced group name strings; caller frees each entry and the array
 * @param[out]  ngroupsp  Number of entries in groupsp
 * @retval      0         OK
 * @retval     -1         Error
 * @note getgrouplist is limited to /etc/group and may not include all groups for a user.
 */
int
user2groups(const char  *username,
            char      ***groupsp,
            int         *ngroupsp)
{
    int            retval = -1;
    char           buf[1024];
    struct passwd  pwbuf;
    struct passwd *pwbufp = NULL;
    struct group  *gr;
    char         **groups = NULL;
    int            ngroups = 0;
    int            i;
    char         **g2;
    char          *gname;

    if (getpwnam_r(username, &pwbuf, buf, sizeof(buf), &pwbufp) != 0){
        clixon_err(OE_UNIX, errno, "getpwnam_r(%s)", username);
        goto done;
    }
    if (pwbufp == NULL){
        clixon_err(OE_UNIX, 0, "No such user: %s", username);
        goto done;
    }
    setgrent();
    while ((gr = getgrent()) != NULL){
        /* Include if this is the user's primary group or username in member list */
        if (gr->gr_gid == pwbufp->pw_gid){
            gname = gr->gr_name;
        }
        else {
            gname = NULL;
            for (i = 0; gr->gr_mem[i] != NULL; i++){
                if (strcmp(gr->gr_mem[i], username) == 0){
                    gname = gr->gr_name;
                    break;
                }
            }
        }
        if (gname == NULL)
            continue;
        if ((g2 = realloc(groups, (ngroups + 1) * sizeof(char *))) == NULL){
            clixon_err(OE_UNIX, errno, "realloc");
            endgrent();
            goto done;
        }
        groups = g2;
        if ((groups[ngroups] = strdup(gname)) == NULL){
            clixon_err(OE_UNIX, errno, "strdup");
            endgrent();
            goto done;
        }
        ngroups++;
    }
    endgrent();
    *groupsp = groups;
    *ngroupsp = ngroups;
    groups = NULL;
    retval = 0;
 done:
    if (groups){
        for (i = 0; i < ngroups; i++)
            if (groups[i])
                free(groups[i]);
        free(groups);
    }
    return retval;
}

/* Privileges drop perm, temp and restore
 * @see https://www.usenix.org/legacy/events/sec02/full_papers/chen/chen.pdf
 */
/*! Temporarily drop privileges 
 *
 * @param[in]  new_uid
 * @retval     0       OK
 * @retval    -1       Error
 */
int
drop_priv_temp(uid_t new_uid)
{
#ifdef HAVE_GETRESUID
    int retval = -1;

    clixon_debug(CLIXON_DBG_PROC, "from uid:%u to %u", getuid(), new_uid);
    if (setresuid(-1, new_uid, geteuid()) < 0){
        clixon_err(OE_UNIX, errno, "setresuid(%u) from %u", new_uid, getuid());
        goto done;
    }
    if (geteuid() != new_uid){
        clixon_err(OE_UNIX, errno, "geteuid");
        goto done;
    }
    retval = 0;
 done:
    return retval;
#else
    clixon_debug(CLIXON_DBG_PROC, "Drop privileges not implemented on this platform since getresuid is not available");
    return 0;
#endif
}

/*! Permanently drop privileges
 *
 * @param[in]  new_uid
 * @retval     0       OK
 * @retval    -1       Error
 */
int
drop_priv_perm(uid_t new_uid)
{
#ifdef HAVE_GETRESUID
    int   retval = -1;
    uid_t ruid;
    uid_t euid;
    uid_t suid;

    clixon_debug(CLIXON_DBG_PROC, "from uid:%u to %u", getuid(), new_uid);
    if (setresuid(new_uid, new_uid, new_uid) < 0){
        clixon_err(OE_UNIX, errno, "setresuid(%u) from %u", new_uid, getuid());
        goto done;
    }
    if (getresuid(&ruid, &euid, &suid) < 0){
        clixon_err(OE_UNIX, errno, "getresuid");
        goto done;
    }
    if (ruid != new_uid ||
        euid != new_uid ||
        suid != new_uid){
        clixon_err(OE_UNIX, EINVAL, "Non-matching uid");
        goto done;
    }
    retval = 0;
 done:
    return retval;
#else
    clixon_debug(CLIXON_DBG_PROC, "Drop privileges not implemented on this platform since getresuid is not available");
    return 0;
#endif
}

/*! Restore privileges to saved level */
int
restore_priv(void)
{
#ifdef HAVE_GETRESUID
    int retval = -1;
    uid_t ruid;
    uid_t euid;
    uid_t suid;

    clixon_debug(CLIXON_DBG_PROC, "");
    if (getresuid(&ruid, &euid, &suid) < 0){
        clixon_err(OE_UNIX, errno, "getresuid");
        goto done;
    }
    if (setresuid(-1, suid, -1) < 0){
        clixon_err(OE_UNIX, errno, "setresuid(%u) from %u", suid, getuid());
        goto done;
    }
    if (geteuid() != suid){
        clixon_err(OE_UNIX, EINVAL, "Non-matching uid");
        goto done;
    }
    retval = 0;
 done:
    return retval;
#else
    clixon_debug(CLIXON_DBG_PROC, "Drop privileges not implemented on this platform since getresuid is not available");
    return 0;
#endif
}
