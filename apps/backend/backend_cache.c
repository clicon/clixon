/*! Remove all files in a dir with optional given pattern
 *
 * @param[in]  dir     Directory
 * @param[in]  subdir  Optional sub-directory
 * @param[in]  pattern Regexp
 * @retval     0       OK
 * @retval    -1       Error
 */
static int
remove_all_files(const char *dir,
                 const char *subdir,
                 const char *pattern)
{
    int            retval = -1;
    struct dirent *dp = NULL;
    int            ndp;
    cbuf          *cb = NULL;
    int            i;

    if ((cb = cbuf_new()) == NULL){
        clixon_err(OE_UNIX, errno, "cbuf_new");
        goto done;
    }
    cprintf(cb, "%s", dir);
    if (subdir)
        cprintf(cb, "/%s", subdir);
    if ((ndp = clicon_file_dirent(cbuf_get(cb), &dp, "\\.yang$", S_IFREG)) < 0)
        goto done;
    for (i = 0; i < ndp; i++) {
        cbuf_reset(cb);
        cprintf(cb, "%s", dir);
        if (subdir)
            cprintf(cb, "/%s", subdir);
        cprintf(cb, "/%s", dp[i].d_name);
        if (unlink(cbuf_get(cb)) < 0){
            clixon_err(OE_UNIX, errno, "unlink(%s)", cbuf_get(cb));
            goto done;
        }
    }
    retval = 0;
 done:
    if (dp)
        free(dp);
    if (cb)
        cbuf_free(cb);
    return retval;
}

/*! Handle autocli cache rpc
 *
 * @param[in]  h         Clixon handle
 * @param[in]  xe        Request: <rpc><xn></rpc>
 * @param[in]  operation read, write or clear
 * @param[out] cbret     Return xml tree, eg <rpc-reply>..., <rpc-error..
 * @retval     1         OK
 * @retval     0         Fail, rpc error returned in cbret
 * @retval    -1         Error
 */
static int
clixon_cache_autocli(clixon_handle h,
                     cxobj        *xe,
                     const char   *operation,
                     cbuf         *cbret)
{
    int         retval = -1;
    cxobj      *x;
    char       *dir = NULL;
    char       *domain = NULL;
    char       *module = NULL;
    char       *revision = NULL;
    char       *keyword = NULL;
    char       *name = NULL;
    char       *clispec = NULL;
    cbuf       *cberr = NULL;
    cbuf       *cb = NULL;
    char       *filename;
    FILE       *f = NULL;
    char       *str = NULL;
    struct stat fstat;
    size_t      len;

    if ((dir = clicon_option_str(h, "CLICON_CLISPEC_CACHE_DIR")) == NULL){
        if (netconf_operation_failed(cbret, "application", "CLICON_CLISPEC_CACHE_DIR must be set for autocli-cache rpc")< 0)
            goto done;
        goto fail;
    }
    if ((x = xml_find_type(xe, NULL, "domain", CX_ELMNT)) != NULL)
        domain = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "module", CX_ELMNT)) != NULL)
        module = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "revision", CX_ELMNT)) != NULL)
        revision = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "keyword", CX_ELMNT)) != NULL)
        keyword = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "name", CX_ELMNT)) != NULL)
        name = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "clispec", CX_ELMNT)) != NULL)
        clispec = xml_body(x);
    // XXX debug
    fprintf(stderr, "%s %s %s %s %s %s %s %s\n", __FUNCTION__,
            operation, domain, module, revision, keyword, name, clispec);
    if (strcmp(operation, "clear") == 0){
        if (domain == NULL){
            if (netconf_data_missing(cbret, "domain is missing but required for rpc clixon-cache autocli clear") < 0)
                goto done;
            goto fail;
        }
        if (remove_all_files(dir, domain, "\\.yang$") < 0)
            goto done;
    }
    else if (strcmp(operation, "read") == 0){
        if (domain == NULL || module == NULL || revision == NULL){
            if (netconf_missing_element(cbret, "protocol", "domain or module or revision", "Mandatory when operation is read") < 0)
                goto done;
            goto fail;
        }
        if ((cb = cbuf_new()) == NULL){
            clixon_err(OE_UNIX, errno, "cbuf_new");
            goto done;
        }
        cprintf(cb, "%s/%s/%s@%s", dir, domain, module, revision);
        if (keyword && name)
            cprintf(cb, "-%s-%s", keyword, name);
        cprintf(cb, ".cli");
        filename = cbuf_get(cb);
        if (stat(filename, &fstat) < 0) { /* File does not exist or should be written*/
            if ((cberr = cbuf_new()) == NULL){
                clixon_err(OE_UNIX, errno, "cbuf_new");
                goto done;
            }
            cprintf(cberr, "%s: %s", filename, strerror(errno));
            if (netconf_data_missing(cbret, cbuf_get(cberr)) < 0)
                goto done;
            goto fail;
        }
        if ((f = fopen(filename, "r")) == NULL){
            clixon_err(OE_UNIX, errno, "fopen(%s)", filename);
            goto done;
        }
        if ((str = calloc(fstat.st_size+1, 1)) == NULL){
            clixon_err(OE_UNIX, errno, "calloc");
            goto done;
        }
        len = fread(str, 1, fstat.st_size, f);
        if (len != fstat.st_size){
            clixon_err(OE_UNIX, errno, "fread %lu != %lu", len, fstat.st_size);
            goto done;
        }
        cprintf(cbret, "<rpc-reply xmlns=\"%s\"><clispec xmlns=\"%s\">",
                NETCONF_BASE_NAMESPACE, CLIXON_LIB_NS);
        if (cbuf_append_str(cbret, str) < 0){
            clixon_err(OE_UNIX, errno, "cbuf_append_str");
            goto done;
        }
        cprintf(cbret, "</clispec></rpc-reply>");
    }
    else {
        if ((cberr = cbuf_new()) == NULL){
            clixon_err(OE_UNIX, errno, "cbuf_new");
            goto done;
        }
        cprintf(cberr, "%s not yet implemented", operation);
        if (netconf_invalid_value(cbret, "application", cbuf_get(cberr)) < 0)
            goto done;
        goto fail;
    }
    cprintf(cbret, "<rpc-reply xmlns=\"%s\"><ok/></rpc-reply>", NETCONF_BASE_NAMESPACE);
    retval = 1;
 done:
    if (f)
        fclose(f);
    if (cberr)
        cbuf_free(cberr);
    if (cb)
        cbuf_free(cb);
    if (str)
        free(str);
    return retval;
 fail:
    retval = 0;
    goto done;
}

/*! Handle yang-domain cache rpc
 *
 * @param[in]  h         Clixon handle
 * @param[in]  xe        Request: <rpc><xn></rpc>
 * @param[in]  operation read, write or clear
 * @param[out] cbret     Return xml tree, eg <rpc-reply>..., <rpc-error..
 * @retval     1         OK
 * @retval     0         Fail, rpc error returned in cbret
 * @retval    -1         Error
 */
static int
clixon_cache_yang_domain(clixon_handle h,
                         cxobj        *xe,
                         const char   *operation,
                         cbuf         *cbret)
{
    int            retval = -1;
    char          *dir;
    cxobj         *x;
    char          *domain;
    cbuf          *cberr = NULL;
    cbuf          *cb = NULL;

    if ((x = xml_find_type(xe, NULL, "domain", CX_ELMNT)) != NULL)
        domain = xml_body(x);
    if (strcmp(operation, "clear") == 0){
        if (domain == NULL){
            if (netconf_data_missing(cbret, "domain is missing but required for rpc clixon-cache yang-domain clear") < 0)
                goto done;
            goto fail;
        }
        if ((dir = clicon_yang_domain_dir(h)) == NULL){
            if (netconf_invalid_value(cbret, "application", "CLICON_YANG_DOMAIN_DIR not set") < 0)
                goto done;
            goto fail;
        }
        if (remove_all_files(dir, domain, "\\.yang$") < 0)
            goto done;
    }
    else {
        if ((cberr = cbuf_new()) == NULL){
            clixon_err(OE_UNIX, errno, "cbuf_new");
            goto done;
        }
        cprintf(cberr, "%s not yet implemented", operation);
        if (netconf_invalid_value(cbret, "application", cbuf_get(cberr)) < 0)
            goto done;
        goto fail;
    }
    cprintf(cbret, "<rpc-reply xmlns=\"%s\"><ok/></rpc-reply>", NETCONF_BASE_NAMESPACE);
    retval = 1;
 done:
    if (cb)
        cbuf_free(cb);
    if (cberr)
        cbuf_free(cberr);
    return retval;
 fail:
    retval = 0;
    goto done;
}

