/*! Perform operations on cache data
 *
 * @param[in]  h       Clixon handle
 * @param[in]  xe      Request: <rpc><xn></rpc>
 * @param[out] cbret   Return xml tree, eg <rpc-reply>..., <rpc-error..
 * @param[in]  arg     client-entry
 * @param[in]  regarg  User argument given at rpc_callback_register()
 * @retval     0       OK
 * @retval    -1       Error
 */
int
from_client_clixon_cache(clixon_handle h,
                         cxobj        *xe,
                         cbuf         *cbret,
                         void         *arg,
                         void         *regarg)
{
    int         retval = -1;
    cxobj      *x;
    char       *operation = NULL;
    char       *type = NULL;
    cbuf       *cberr = NULL;

    if ((x = xml_find_type(xe, NULL, "operation", CX_ELMNT)) == NULL){
        clixon_err(OE_YANG, 0, "operation is missing but is mandatory in rpc clixon-cache");
        goto done;
    }
    operation = xml_body(x);
    if ((x = xml_find_type(xe, NULL, "type", CX_ELMNT)) == NULL){
        clixon_err(OE_YANG, 0, "type is missing but is mandatory in rpc clixon-cache");
        goto done;
    }
    type = xml_body(x);
    if (strcmp(type, "autocli") == 0){
        if (clixon_cache_autocli(h, xe, operation, cbret) < 0)
            goto done;
    }
    else if (strcmp(type, "yang-domain") == 0){
        if (clixon_cache_yang_domain(h, xe, operation, cbret) < 0)
            goto done;
    }
    else if (strcmp(type, "datastore") == 0){
        if (clixon_cache_xmldb(h, xe, operation, cbret) < 0)
            goto done;
    }
    else{
        if ((cberr = cbuf_new()) == NULL){
            clixon_err(OE_UNIX, errno, "cbuf_new");
            goto done;
        }
        cprintf(cberr, "%s not a valid cache-type", type);
        if (netconf_invalid_value(cbret, "application", cbuf_get(cberr)) < 0)
            goto done;
        goto ok;
    }
 ok:
    retval = 0;
 done:
    if (cberr)
        cbuf_free(cberr);
    return retval;
}
