/*!
 * @brief Spawns a new process and runs the specified program within it
 *
 * The function creates a new child process and runs the specified program within it.
 * Before launching the program in the child process, environment variables taken from cvv are set.
 *
 * The function checks the parameters passed to it. Situations where at least one function argument
 * is missing, or when two arguments are present in both the function and the cvv vector are considered invalid.
 *
 * Example usage:
 * @code
 *      run_program_err("Run program"), cli_start_program();
 *      run_program_python3("Run program"), cli_start_program("python3");
 *      run_program_python3_source_arg("Run program"), cli_start_program("python3", "/tmp/test.py");
 *      run_program_python3_source_arg_vector("Run program") <source:rest>("Path program"), cli_start_program("python3");
 *      run_program_python3_source_arg_vector_err("Run program") <source:rest>("Path program"), cli_start_program("python3", "/tmp/test2.py");
 *      run_program_bash("Run program"), cli_start_program("bash");
 * @endcode
 *
 * @param[in] h Clixon handle
 * @param[in] cvv Vector of command variables
 * @param[in] argv Function arguments
 * @retval   0 OK, returns the exit code of the program
 * @retval  -1 Error
 *
 */
int
cli_start_program(clixon_handle h,
                  cvec         *cvv,
                  cvec         *argv)
{
    int pid = 0;
    int retval = -1;
    char *script_path = NULL;
    char *runner = NULL;
    char *buf = NULL;
    char *work_dir = NULL, *reserve_path = NULL;
    struct passwd pw, *pwresult = NULL;
    size_t bufsize;
    int s;

    /* Check parameters */
    if (cvec_len(argv) == 0){
        clixon_err(OE_PLUGIN, EINVAL, "Can not found argument in a function");
        goto done;
    }
    if ((cvec_len(argv) >= 2) && (cvec_len(cvv) >= 2)){
        clixon_err(OE_PLUGIN, EINVAL, "A lot of arguments");
        goto done;
    }
    if ((cvec_len(argv) == 2) && (cvec_len(cvv) == 2)){
        clixon_err(OE_PLUGIN, EINVAL, "You cannot use 2 arguments in a function and 1 argument in a vector");
        goto done;
    }

    /* get data */
    if (cvec_len(argv) == 1){
        runner = cv_string_get(cvec_i(argv, 0));
    }
    if (cvec_len(argv) == 2){
        runner = cv_string_get(cvec_i(argv, 0));
        script_path = cv_string_get(cvec_i(argv, 1));
    }
    if (cvec_len(cvv) == 2){
        script_path = cv_string_get(cvec_i(cvv, 1));
    }
    if (script_path){
        reserve_path = strdup(script_path);
        work_dir = dirname(reserve_path);
    }
    bufsize = sysconf(_SC_GETPW_R_SIZE_MAX);
    if (bufsize == -1){
        bufsize = 16384;
    }
    buf = malloc(bufsize);
    if (buf == NULL) {
        perror("malloc");
        goto done;
    }

    s = getpwuid_r(getuid(), &pw, buf, bufsize, &pwresult);
    if (pwresult == NULL) {
        if (s == 0)
            clixon_err(OE_PLUGIN, errno, "getpwuid_r");
        else
            perror("getpwuid_r");
        goto done;
    }

    /* main run */
    if ((pid = fork()) == 0) {
        /* child process */
        if ((work_dir ? chdir(work_dir) : chdir(pw.pw_dir)) < 0) {
            clixon_err(OE_PLUGIN, errno, "chdir");
        }
        execlp(runner, runner, script_path, NULL);
        clixon_err(OE_PLUGIN, errno, "Error run script");
        return -1;
    }else if(pid == -1){
        clixon_err(OE_PLUGIN, errno, "fork");
    }else{
        /* parent process */
        int status;
        if (waitpid(pid, &status, 0) != pid ){
            clixon_err(OE_PLUGIN, errno, "waitpid error");
            goto done;
        } else {
            return WEXITSTATUS(status);
        }
    }
    retval = 0;
    done:
        if(buf)
            free(buf);
        if(reserve_path)
            free(reserve_path);
        return retval;
}

/*! Start bash from cli callback
 *
 * Typical usage: shell("System Bash") <source:rest>, cli_start_shell();
 * @param[in]   h     Clixon handle
 * @param[in]   cvv   Vector of command variables
 * @param[in]   argv  [<shell>], defaults to "sh"
 * @retval      0     OK
 * @retval     -1     Error
 */
int
cli_start_shell(clixon_handle h,
                cvec         *cvv,
                cvec         *argv)
{
    int            retval = -1;
    char          *cmd;
    char          *shcmd = "sh";
    struct passwd *pw;
    char           bcmd[128];
    cg_var        *cv1 = cvec_i(cvv, 1);
    sigset_t       oldsigset;
    struct sigaction oldsigaction[32] = {{{0,},},};

    if (cvec_len(argv) > 1){
        clixon_err(OE_PLUGIN, EINVAL, "Received %d arguments. Expected: [<shell>]",
                   cvec_len(argv));
        goto done;
    }
    if (cvec_len(argv) == 1){
        shcmd = cv_string_get(cvec_i(argv, 0));
    }
    cmd = (cvec_len(cvv)>1 ? cv_string_get(cv1) : NULL);
    if ((pw = getpwuid(getuid())) == NULL){
        clixon_err(OE_UNIX, errno, "getpwuid");
        goto done;
    }
    if (chdir(pw->pw_dir) < 0){
        clixon_err(OE_UNIX, errno, "chdir");
        endpwent();
        goto done;
    }
    endpwent();

    if (clixon_signal_save(&oldsigset, oldsigaction) < 0)
        goto done;
    cli_signal_flush(h);
    cli_signal_unblock(h);
    if (cmd){
        snprintf(bcmd, 128, "%s -c \"%s\"", shcmd, cmd);
        if (system(bcmd) < 0){
            cli_signal_block(h);
            clixon_err(OE_UNIX, errno, "system(bash -c)");
            goto done;
        }
    }
    else{
        snprintf(bcmd, 128, "%s ", shcmd); /* -l (login shell) but is applicable to bash only */
        if (system(bcmd) < 0){
            cli_signal_block(h);
            clixon_err(OE_UNIX, errno, "system(bash)");
            goto done;
        }
    }
    cli_signal_block(h);
#if 0 /* Allow errcodes from bash */
    if (retval != 0){
        clixon_err(OE_UNIX, errno, "system(%s) %d", cmd, retval);
        goto done;
    }
#endif
    if (clixon_signal_restore(&oldsigset, oldsigaction) < 0)
        goto done;
    retval = 0;
 done:
    return retval;
}

